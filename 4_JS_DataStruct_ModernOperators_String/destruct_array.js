const books = require('./books');

// -------------------------------------------------------------------------------------------------------------------------------------------
// 1.1: Destructure books array into 2 variables
let [firstBook, secondBook] = books;
console.log(firstBook, secondBook);
// -------------------------------------------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------------------------------------------
// 1.2: Destructure 3rd book in books array into thirdBook
let [, , thirdBook] = books;
console.log(thirdBook);
// -------------------------------------------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------------------------------------------
// 1.3: Destructure nested array
const ratings = [
  ['rating', 4.19],
  ['ratingsCount', 144584],
];
let [[, rating], [, ratingsCount]] = ratings;
console.log(rating, ratingsCount);
// -------------------------------------------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------------------------------------------
// 1.4: Destructure array with default values
const ratingStars = [63405, 1808];
let [fiveStarRatings, oneStarRatings, threeStarRatings = 0] = ratingStars;
console.log(fiveStarRatings, oneStarRatings, threeStarRatings);
// -------------------------------------------------------------------------------------------------------------------------------------------
