const gameEvents = new Map([
  [17, '⚽ GOAL'],
  [36, '🔁 Substitution'],
  [47, '⚽ GOAL'],
  [61, '🔁 Substitution'],
  [64, '🔶 Yellow card'],
  [69, '🔴 Red card'],
  [70, '🔁 Substitution'],
  [72, '🔁 Substitution'],
  [76, '⚽ GOAL'],
  [80, '⚽ GOAL'],
  [92, '🔶 Yellow card'],
]);

// -------------------------------------------------------------------------------------------------------------------------------------------
// 1. Create an array called 'events' without duplicates
const events = [...new Set(gameEvents.values())];
console.log(events);
// -------------------------------------------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------------------------------------------
// 2. remove 64th minute yellow card from gameEvent logs
gameEvents.delete(64);
console.log(gameEvents);
// -------------------------------------------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------------------------------------------
// 3. Calculate average time between events for the game (90-minutes game)
console.log(
  `An event happened, on average, every ${90 / gameEvents.size} minutes`
);
// -------------------------------------------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------------------------------------------
// 4. Loop over gameEvents & log each element to console, marking first half or second half
for (let [key, val] of gameEvents) {
  let status = key <= 45 ? '[FIRST HALF]' : '[SECOND HALF]';
  console.log(`${status} ${key}: ${val}`);
}
